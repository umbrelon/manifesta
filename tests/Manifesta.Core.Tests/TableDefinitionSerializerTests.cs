    // ── Round-trip via TableLoader deserialization ─────────────────────────────

    [Fact]
    public async Task Serialize_RoundTrip_DeserializesBackToEquivalentDefinition()
    {
        var original = new TableDefinition
        {
            Name          = "dbo.Bundle",
            Description   = "Product bundle",
            Fields        =
            [
                new FieldDefinition { Name = "Id",          Type = "int",           IsPrimaryKey = true,  Nullable = false },
                new FieldDefinition { Name = "Description", Type = "nvarchar(200)", Nullable = true,  Description = "Bundle description" },
                new FieldDefinition { Name = "StatusId",    Type = "int",           Nullable = false, IsMatchColumn = true, Default = "1" },
            ],
            PrimaryKey    = ["Id"],
            ForeignKeys   =
            [
                new ForeignKey { SourceField = "StatusId", TargetTable = "dbo.BundleStatus", TargetField = "Id", CascadeDelete = true },
            ],
            DatabaseTypes = ["MSSQL"],
            Sections      = ["bundles"],
        };

        // Serialize to JSON, write to an isolated temp directory, load back via TableLoader
        var json   = TableDefinitionSerializer.Serialize(original);
        var tmpDir = Path.Combine(Path.GetTempPath(), Path.GetRandomFileName());
        Directory.CreateDirectory(tmpDir);
        try
        {
            var tmpFile = Path.Combine(tmpDir, "dbo.Bundle.json");
            await File.WriteAllTextAsync(tmpFile, json);

            var loader  = new TableLoader();
            var results = await loader.LoadAsync(tmpDir);

            results.Should().HaveCount(1);
            var loaded = results[0];

            loaded!.Name.Should().Be(original.Name);
            loaded.Description.Should().Be(original.Description);
            loaded.PrimaryKey.Should().Equal(original.PrimaryKey);
            loaded.DatabaseTypes.Should().Equal(original.DatabaseTypes);
            loaded.Sections.Should().Equal(original.Sections);

            loaded.Fields.Should().HaveCount(3);
            loaded.Fields[0].Name.Should().Be("Id");
            loaded.Fields[0].IsPrimaryKey.Should().BeTrue();
            loaded.Fields[1].Description.Should().Be("Bundle description");
            loaded.Fields[2].IsMatchColumn.Should().BeTrue();
            loaded.Fields[2].Default.Should().Be("1");

            loaded.ForeignKeys.Should().HaveCount(1);
            loaded.ForeignKeys[0].CascadeDelete.Should().BeTrue();
            loaded.ForeignKeys[0].Kind.Should().Be(ForeignKeyKind.Physical);
        }
        finally
        {
            Directory.Delete(tmpDir, recursive: true);
        }
    }

