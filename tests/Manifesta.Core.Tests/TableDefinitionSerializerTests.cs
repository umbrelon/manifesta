    [Fact]
    public async Task Serialize_RoundTrip_ReferenceTableWithData_PreservesAll()
    {
        var doc = JsonDocument.Parse("[{\"Id\":1,\"State\":\"Active\"},{\"Id\":2,\"State\":\"Inactive\"}]").RootElement;
        var original = new TableDefinition
        {
            Name             = "dbo.Status",
            IsReferenceTable = true,
            Fields           =
            [
                new FieldDefinition { Name = "Id",    Type = "int",          Nullable = false },
                new FieldDefinition { Name = "State", Type = "varchar(20)",  Nullable = false },
            ],
            PrimaryKey = ["Id"],
            Data       =
            [
                new Dictionary<string, JsonElement> { ["Id"] = doc[0].GetProperty("Id"), ["State"] = doc[0].GetProperty("State") },
                new Dictionary<string, JsonElement> { ["Id"] = doc[1].GetProperty("Id"), ["State"] = doc[1].GetProperty("State") },
            ],
        };

        var json   = TableDefinitionSerializer.Serialize(original);
        var tmpDir = Path.Combine(Path.GetTempPath(), Path.GetRandomFileName());
        Directory.CreateDirectory(tmpDir);
        try
        {
            await File.WriteAllTextAsync(Path.Combine(tmpDir, "dbo.Status.json"), json);

            var loader  = new TableLoader();
            var results = await loader.LoadAsync(tmpDir);
            var loaded  = results.Single();

            loaded.IsReferenceTable.Should().BeTrue();
            loaded.Data.Should().HaveCount(2);
            loaded.Data[0]["Id"].GetInt32().Should().Be(1);
            loaded.Data[0]["State"].GetString().Should().Be("Active");
            loaded.Data[1]["Id"].GetInt32().Should().Be(2);
            loaded.Data[1]["State"].GetString().Should().Be("Inactive");
        }
        finally
        {
            Directory.Delete(tmpDir, recursive: true);
        }
    }

